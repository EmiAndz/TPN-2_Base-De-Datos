INSERT INTO paradas_intermedias (id_viaje, orden, direccion_parada)
SELECT v.id_viaje, o.orden,
       CASE o.orden
         WHEN 1 THEN CONCAT('Parada A - viaje ', v.id_viaje)
         WHEN 2 THEN CONCAT('Parada B - viaje ', v.id_viaje)
       END AS direccion_parada
FROM (SELECT id_viaje FROM viajes ORDER BY id_viaje LIMIT 15) AS v
CROSS JOIN (SELECT 1 AS orden UNION ALL SELECT 2) AS o;

